s = gets.chomp.to_i
num = s.to_s.split("").map(&:to_i)
sum = 0
n = num.length

(1..n).each do |digit| #桁数
    (0..n-1).each do |i| #インデックス
        next if digit > (n-i)
        dig = num[i] * 10**(digit-1)
        left = i
        right = (n-1 - (i+digit) > 0 ? n-1 - (i+digit): 0)
        sum += dig * (2 ** (left + right))
    end
end
puts sum

